KNOWLEDGE = """
# תאימות מסחר אלקטרוני ישראלי — ידע מקצועי

## הבעיה שהסקיל פותר
חנות מקוונת בישראל חייבת לעמוד בחוק הגנת הצרכן, תקנות מכר מרחוק, חובת הנפקת חשבוניות ודרישות נגישות. הדרישות מפוזרות בין חוקים שונים ומתעדכנות תכופות.

## 1. זכות ביטול עסקה (חוק הגנת הצרכן)
- **14 יום** ביטול לרוב הרכישות אונליין
- **30 יום** בעסקאות מסוימות עם קשישים/אנשים עם מוגבלות
- על המוכר להחזיר כסף תוך **14 יום** מקבלת הבקשה
- החזרת המוצר — על חשבון הצרכן (אלא אם פגום)

**מוצרים שלא ניתן להחזיר:**
- תוכנה/תוכן דיגיטלי שנפתח
- מוצרים שנשלחו לפי הזמנה אישית
- מזון שהוכן מיוחד
- מוצרי בריאות/היגיינה שנפתחו

## 2. הצגת מחירים (חובה חוקית)
- **מחיר מלא כולל מע"מ** (17%) חייב להיות מוצג בדף המוצר
- עלות משלוח — חייבת להיות ברורה לפני סיום הרכישה
- אסור "מחיר מפתיע" בקופה
- חשבונית מס חייבת להינפק לכל עסקה

## 3. נגישות — תקן IS 5568 (ת"י 5568)
- **חל על כל אתרי מסחר אלקטרוני** (לא רק על עסקים גדולים)
- מבוסס על WCAG 2.1 רמה AA
- דרישות עיקריות:
  - טקסט חלופי לתמונות
  - ניווט מקלדת מלא
  - ניגודיות צבעים מספקת
  - כיתובים לווידאו
  - הצהרת נגישות באתר
- מועד אכיפה: אתרים קיימים — 23 אוקטובר 2021 ואילך

## 4. הסכמת עוגיות ופרטיות
- אתרי e-commerce ישראליים חייבים לקבל הסכמה לפני שמירת עוגיות שיווקיות
- מדיניות הפרטיות חייבת להיות בעברית ונגישה בלחיצה
- הרשות להגנת הפרטיות (PPA) פרסמה הנחיות ספציפיות

## 5. PCI DSS 4.0 (מאפריל 2025 — חובה)
- כל חנות המעבדת נתוני כרטיסי אשראי ישירות חייבת בציות PCI DSS 4.0
- פתרון: שימוש בשערי תשלום מאושרים (PayPal, Stripe, Cardcom, Tranzila) שנושאים את האחריות

## 6. גילוי עסקי חובה
חייב להיות מוצג באתר:
- שם העסק המלא ומספר ח.פ / ע.מ
- כתובת פיזית וטלפון
- מדיניות ביטול/החזרה ברורה בעברית
- תנאי שימוש בעברית
- הצהרת נגישות

## 7. אישור הזמנה
- אישור אוטומטי במייל תוך זמן סביר מחויב
- האישור חייב לכלול: פרטי המוצר, מחיר, פרטי הספקה

## 8. מע"מ והנהלת חשבונות
- מע"מ: 17% (כפוף לשינויים)
- חשבונית מס דיגיטלית: חייבת לעמוד בדרישות רשות המסים
- דיווח: הכנסות ממסחר אלקטרוני ממוסות כרגיל

## רשימת בדיקה לחנות מקוונת
- [ ] מחיר כולל מע"מ בדף המוצר
- [ ] עלות משלוח לפני סיום הרכישה
- [ ] מדיניות ביטול/החזרה ברורה (14 יום)
- [ ] תנאי שימוש בעברית
- [ ] מדיניות פרטיות בעברית
- [ ] הצהרת נגישות (ת"י 5568)
- [ ] הסכמת עוגיות
- [ ] גילוי עסקי מלא (שם, ח.פ, כתובת, טלפון)
- [ ] אישור הזמנה אוטומטי במייל
- [ ] עמידה ב-PCI DSS (אם מעבדים כרטיסי אשראי ישירות)
"""


